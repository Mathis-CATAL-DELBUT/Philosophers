#include "philo.h"

long long int	ft_atoll(const char *str)
{
	int i;
	int neg;
	long long int n;

	i = 0;
	neg = 1;
	n = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - 48);
		if (n * neg < -92233720368.54775808 * 100000000
        || n * neg > 92233720368.54775807 * 100000000)
			return (0);
		i++;
	}
	return (n * neg);
}